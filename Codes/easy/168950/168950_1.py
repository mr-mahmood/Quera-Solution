spring = ['september', 'october', 'november']
summer = ['december', 'january', 'february']
Autumn = ['march', 'april', 'may']
winter = ['june', 'july', 'august']

month = input()

if month in spring:
    print('spring')
elif month in summer:
    print('summer')
elif month in Autumn:
    print('autumn')
else:
    print('winter')

